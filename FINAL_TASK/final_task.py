def get_image(data):
    k = int(''.join(data.split()))
    image = np.zeros((height, width), dtype=bool)
    for y in range(height - 1, -1, -1):
        for x in range(width):
            val = k + y
            image[-y - 1, x] = (val // (17 * (2 ** (17 * x + (val % 17))))) % 2 > 0.5
    return image


